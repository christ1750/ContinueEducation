package edu.neu.nxist.continuingedu.project.model;

public class ProjectSponsor {

	private int pjSponsorId;
	private String pjSponsorName;

	public int getPjSponsorId() {
		return pjSponsorId;
	}

	public void setPjSponsorId(int pjSponsorId) {
		this.pjSponsorId = pjSponsorId;
	}

	public String getPjSponsorName() {
		return pjSponsorName;
	}

	public void setPjSponsorName(String pjSponsorName) {
		this.pjSponsorName = pjSponsorName;
	}

}
